"""
Trading Performance Analyzer
============================
Reads a trade history CSV and produces a weekly performance review:

  1. Overall statistics (win rate, expectancy, total P&L, etc.)
  2. Performance by setup type
  3. Performance by time of day (hourly blocks)
  4. Performance by day of week
  5. Pattern detection (best/worst hour, best/worst setup, consistent losers)
  6. Written summary in plain English
  7. Data-driven recommendations

Usage:
    python reporting/analyze_trades.py reports/04-19-2026_04-25-2026_tradelog.csv
    python reporting/analyze_trades.py reports/04-19-2026_04-25-2026_tradelog.csv --output reports/analysis.txt

Required CSV columns (case-insensitive, flexible naming):
    Symbol, Entry Date, Entry Time, Entry Price, Exit Date, Exit Time,
    Exit Price, Shares, P&L, Setup Type (or Strategy)

Generated by Opus 4, integrated and fixed by Claude Sonnet.
"""

