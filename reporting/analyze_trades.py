def load_trades(csv_path: Path) -> pd.DataFrame:
    """Load the CSV, normalize column names, parse types, and validate."""
    if not csv_path.exists():
        raise FileNotFoundError(f"CSV file not found: {csv_path}")

    df = pd.read_csv(csv_path)

    # Normalize column names first
    df.columns = [c.strip().lower() for c in df.columns]

    # Stop at summary footer rows
    if "symbol" in df.columns:
        df = df[df["symbol"].notna() & (df["symbol"].astype(str).str.strip() != "")]
        df = df[~df["symbol"].astype(str).str.startswith("---")]
        # Also drop rows where symbol looks like a summary label
        summary_labels = {"total trades", "winning trades", "losing trades",
                         "win rate", "total p&l", "period", "generated"}
        df = df[~df["symbol"].astype(str).str.lower().isin(summary_labels)]

    # Map aliases
    df = df.rename(columns={c: COLUMN_ALIASES.get(c, c) for c in df.columns})

    # Validate required columns
    missing = REQUIRED_COLUMNS - set(df.columns)
    if missing:
        raise ValueError(
            f"CSV is missing required columns: {sorted(missing)}.\n"
            f"Found columns: {sorted(df.columns)}"
        )

    # Parse entry_time into time-of-day
    df["entry_time_parsed"] = pd.to_datetime(
        df["entry_time"].astype(str), errors="coerce", format="mixed"
    ).dt.time

    # Parse entry_date for day-of-week
    df["entry_date_parsed"] = pd.to_datetime(
        df["entry_date"].astype(str), errors="coerce", format="mixed"
    )

    # Coerce P&L — strip $, commas, + signs
    df["pnl"] = pd.to_numeric(
        df["pnl"].astype(str).str.replace(r"[$,+]", "", regex=True),
        errors="coerce",
    )
    df["shares"] = pd.to_numeric(df["shares"], errors="coerce")

    # Normalize setup_type
    df["setup_type"] = df["setup_type"].astype(str).str.strip()

    # Drop rows that failed to parse
    bad = df[df["pnl"].isna() | df["entry_time_parsed"].isna() | df["entry_date_parsed"].isna()]
    if len(bad):
        print(f"WARNING: Dropped {len(bad)} row(s) with unparseable date/time/PnL.",
              file=sys.stderr)
        df = df.drop(bad.index)

    if df.empty:
        raise ValueError("No valid trades remain after parsing. Check CSV format.")

    return df.reset_index(drop=True)


