def generate_summary(overall: Stats, by_setup: pd.DataFrame, by_hour: pd.DataFrame,
                     by_dow: pd.DataFrame, findings: dict[str, str],
                     min_trades: int = 3) -> tuple[str, list[str]]:
    summary_lines: list[str] = []
    recs: list[str] = []

    if overall.trades == 0:
        return "No trades to analyze.", []

    pnl_sign = "profit" if overall.total_pnl > 0 else "loss" if overall.total_pnl < 0 else "breakeven"
    summary_lines.append(
        f"Across {overall.trades} trades you produced a total {pnl_sign} of "
        f"${overall.total_pnl:,.2f}, with a {overall.win_rate:.1f}% win rate "
        f"and an expectancy of ${overall.expectancy:,.2f} per trade."
    )

    if overall.avg_loss < 0:
        r_ratio = abs(overall.avg_win / overall.avg_loss)
        summary_lines.append(
            f"Your average winner is ${overall.avg_win:,.2f} and average loser is "
            f"${overall.avg_loss:,.2f}, a {r_ratio:.2f}:1 win/loss ratio."
        )
        if r_ratio < 1.0 and overall.win_rate < 60:
            recs.append(
                f"Your win/loss ratio is {r_ratio:.2f}:1 and win rate is "
                f"{overall.win_rate:.1f}%. You need EITHER a higher win rate "
                "OR larger winners relative to losers. Consider tightening "
                "stops, letting winners run longer, or being more selective on entries."
            )
        elif r_ratio >= 2.0:
            recs.append(
                f"Win/loss ratio of {r_ratio:.2f}:1 is strong -- your winners "
                "meaningfully outweigh losers. Protect this edge by not "
                "loosening stops or exiting winners too early."
            )

    if not by_setup.empty:
        qual_setups = by_setup[by_setup["Trades"] >= min_trades]
        if not qual_setups.empty:
            winning = qual_setups[qual_setups["Total P&L"] > 0]
            losing  = qual_setups[qual_setups["Total P&L"] < 0]
            win_verb  = "is" if len(winning) == 1 else "are"
            lose_verb = "is" if len(losing)  == 1 else "are"
            summary_lines.append(
                f"Of {len(qual_setups)} setup(s) with >= {min_trades} trades, "
                f"{len(winning)} {win_verb} profitable and {len(losing)} {lose_verb} "
                f"losing money."
            )
            if not losing.empty:
                worst = losing.loc[losing["Total P&L"].idxmin()]
                recs.append(
                    f"'{worst['setup_type']}' is costing you ${abs(worst['Total P&L']):,.2f} "
                    f"over {worst['Trades']} trades ({worst['Win Rate %']}% win rate). "
                    "Consider paper-trading this setup until it proves out, "
                    "or dropping it from your playbook entirely."
                )
            if not winning.empty:
                best = winning.loc[winning["Total P&L"].idxmax()]
                recs.append(
                    f"'{best['setup_type']}' is your highest-P&L setup "
                    f"(${best['Total P&L']:,.2f}, expectancy ${best['Expectancy']:,.2f}). "
                    "Consider allocating more size or more attention here."
                )

    if not by_hour.empty:
        qual_hours = by_hour[by_hour["Trades"] >= min_trades]
        if not qual_hours.empty:
            best_h  = qual_hours.loc[qual_hours["Total P&L"].idxmax()]
            worst_h = qual_hours.loc[qual_hours["Total P&L"].idxmin()]
            if best_h["time_block"] != worst_h["time_block"]:
                summary_lines.append(
                    f"Your best time block is {best_h['time_block']} "
                    f"(${best_h['Total P&L']:,.2f}) and worst is "
                    f"{worst_h['time_block']} (${worst_h['Total P&L']:,.2f})."
                )
                if worst_h["Total P&L"] < 0:
                    recs.append(
                        f"You're consistently losing during {worst_h['time_block']} "
                        f"(${worst_h['Total P&L']:,.2f} over {worst_h['Trades']} trades). "
                        "Consider sitting out this block, or at minimum cutting "
                        "size until you identify what's different."
                    )

    if not by_dow.empty:
        qual_dow    = by_dow[by_dow["Trades"] >= min_trades]
        losing_days = qual_dow[qual_dow["Total P&L"] < 0]
        if not losing_days.empty:
            day_list = ", ".join(losing_days["day_of_week"].astype(str).tolist())
            recs.append(
                f"Losing money on: {day_list}. Review whether these days have "
                "a pattern in strategy mix, market conditions, or session timing."
            )

    if overall.trades < 30:
        recs.append(
            f"Sample size is small ({overall.trades} trades). Treat these "
            "findings as directional hints, not conclusions -- revisit after "
            "30+ trades per bucket before making big rule changes."
        )

    return "\n".join(summary_lines), recs


def format_section(title: str) -> str:
    bar = "=" * 78
    return f"\n{bar}\n{title}\n{bar}"


def format_stats_block(stats: Stats) -> str:
    d = stats.as_dict()
    lines = []
    for k, v in d.items():
        if k in ("Trades", "Winners", "Losers", "Win Rate %"):
            lines.append(f"  {k:<18} {v:>12}")
        else:
            lines.append(f"  {k:<18} ${v:>11,.2f}")
    return "\n".join(lines)


def format_table(df: pd.DataFrame) -> str:
    if df.empty:
        return "  (no data)"
    return df.to_string(index=False)


def build_report(df: pd.DataFrame) -> str:
    out: list[str] = []

    # --- 1. Overall --------------------------------------------------------
    overall = compute_stats(df["pnl"])
    out.append(format_section("1. OVERALL STATISTICS"))
    out.append(format_stats_block(overall))

    # --- 2. By setup type --------------------------------------------------
    by_setup = group_stats(df, "setup_type")
    out.append(format_section("2. PERFORMANCE BY SETUP TYPE"))
    out.append(format_table(by_setup))

    # --- 3. By time of day (full 24h, no exclusions) -----------------------
    df = df.copy()
    df["time_block"] = df["entry_time_parsed"].apply(assign_time_block)
    by_hour = group_stats(df, "time_block",
                          order=[lbl for lbl, _, _ in TIME_BLOCKS])

    out.append(format_section("3. PERFORMANCE BY TIME OF DAY (ALL 24 HOURS)"))
    excluded = df["time_block"].isna().sum()
    if excluded > 0:
        out.append(f"  Note: {excluded} trade(s) with unparseable entry time excluded.\n")
    else:
        out.append("  All trades included (stocks + crypto, full 24h coverage).\n")
    out.append(format_table(by_hour))

    # --- 4. By day of week (full 7 days) -----------------------------------
    df["day_of_week"] = df["entry_date_parsed"].dt.day_name()
    by_dow = group_stats(df, "day_of_week", order=DAY_ORDER)
    out.append(format_section("4. PERFORMANCE BY DAY OF WEEK (ALL 7 DAYS)"))
    out.append(format_table(by_dow))

    # --- 5. Pattern detection ----------------------------------------------
    out.append(format_section("5. PATTERN DETECTION"))
    out.append("  (buckets with < 3 trades excluded to avoid small-sample noise)\n")
    findings = detect_patterns(by_hour, by_setup)
    if not findings:
        out.append("  No qualifying buckets for pattern detection.")
    else:
        for k, v in findings.items():
            out.append(f"  {k}: {v}")

    # --- 6. Written summary ------------------------------------------------
    summary, recs = generate_summary(overall, by_setup, by_hour, by_dow, findings)
    out.append(format_section("6. WRITTEN SUMMARY"))
    out.append(summary)

    # --- 7. Recommendations ------------------------------------------------
    out.append(format_section("7. RECOMMENDATIONS"))
    if not recs:
        out.append("  No specific recommendations generated from current data.")
    else:
        for i, rec in enumerate(recs, 1):
            lines = rec.split(". ")
            first = f"  {i}. {lines[0]}"
            rest  = [f"     {l}" for l in lines[1:]] if len(lines) > 1 else []
            out.append(first + ("." if not first.endswith(".") else ""))
            out.extend([r + ("." if not r.endswith(".") else "") for r in rest])
            out.append("")

    return "\n".join(out) + "\n"


