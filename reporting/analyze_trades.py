def main() -> int:
    parser = argparse.ArgumentParser(description="Analyze trading performance from CSV.")
    parser.add_argument("csv_path", type=Path, help="Path to trades CSV file")
    parser.add_argument("--output", "-o", type=Path, default=None,
                        help="Optional path to write the report to (also prints to stdout)")
    args = parser.parse_args()

    try:
        df = load_trades(args.csv_path)
    except (FileNotFoundError, ValueError) as e:
        print(f"ERROR: {e}", file=sys.stderr)
        return 1

    report = build_report(df)
    print(report)

    if args.output:
        args.output.write_text(report)
        print(f"Report saved to: {args.output}")

    return 0


if __name__ == "__main__":
    sys.exit(main())
