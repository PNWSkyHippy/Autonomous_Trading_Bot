def detect_patterns(by_hour: pd.DataFrame, by_setup: pd.DataFrame,
                    min_trades: int = 3) -> dict[str, str]:
    findings: dict[str, str] = {}

    def _rank_by(df, key_col, metric, label_prefix):
        if df.empty:
            return None, None
        qualified = df[df["Trades"] >= min_trades]
        if qualified.empty:
            return (f"[{label_prefix}] Not enough data: no bucket has >= "
                    f"{min_trades} trades.", None)
        best  = qualified.loc[qualified[metric].idxmax()]
        worst = qualified.loc[qualified[metric].idxmin()]
        return best, worst

    res = _rank_by(by_hour, "time_block", "Total P&L", "Hourly")
    if isinstance(res, tuple) and res[0] is not None and not isinstance(res[0], str):
        best_hour, worst_hour = res
        findings["Best Hour"] = (
            f"{best_hour['time_block']}  "
            f"(Total P&L: ${best_hour['Total P&L']:,.2f}, "
            f"Win Rate: {best_hour['Win Rate %']}%, "
            f"Trades: {best_hour['Trades']})"
        )
        if best_hour["time_block"] != worst_hour["time_block"]:
            findings["Worst Hour"] = (
                f"{worst_hour['time_block']}  "
                f"(Total P&L: ${worst_hour['Total P&L']:,.2f}, "
                f"Win Rate: {worst_hour['Win Rate %']}%, "
                f"Trades: {worst_hour['Trades']})"
            )
    elif isinstance(res, tuple) and isinstance(res[0], str):
        findings["Hourly Note"] = res[0]

    res = _rank_by(by_setup, "setup_type", "Total P&L", "Setup")
    if isinstance(res, tuple) and res[0] is not None and not isinstance(res[0], str):
        best_setup, worst_setup = res
        findings["Best Setup"] = (
            f"{best_setup['setup_type']}  "
            f"(Total P&L: ${best_setup['Total P&L']:,.2f}, "
            f"Win Rate: {best_setup['Win Rate %']}%, "
            f"Expectancy: ${best_setup['Expectancy']:,.2f}, "
            f"Trades: {best_setup['Trades']})"
        )
        if best_setup["setup_type"] != worst_setup["setup_type"]:
            findings["Worst Setup"] = (
                f"{worst_setup['setup_type']}  "
                f"(Total P&L: ${worst_setup['Total P&L']:,.2f}, "
                f"Win Rate: {worst_setup['Win Rate %']}%, "
                f"Expectancy: ${worst_setup['Expectancy']:,.2f}, "
                f"Trades: {worst_setup['Trades']})"
            )
    elif isinstance(res, tuple) and isinstance(res[0], str):
        findings["Setup Note"] = res[0]

    losing_buckets = []
    if not by_hour.empty:
        candidates = by_hour[
            (by_hour["Trades"] >= min_trades)
            & (by_hour["Total P&L"] < 0)
            & (by_hour["Win Rate %"] < 40)
        ]
        for _, row in candidates.iterrows():
            losing_buckets.append(
                f"{row['time_block']}: "
                f"${row['Total P&L']:,.2f} over {row['Trades']} trades "
                f"({row['Win Rate %']}% win rate)"
            )
    findings["Consistent Loss Periods"] = (
        "; ".join(losing_buckets) if losing_buckets
        else "None flagged (no qualifying bucket had both negative P&L and <40% win rate)."
    )

    return findings


