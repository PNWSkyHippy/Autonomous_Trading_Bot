def assign_time_block(t: dtime | None) -> str | None:
    """
    Map a time-of-day to a named block.
    Now covers full 24 hours so crypto trades are never excluded.
    Midnight (00:00:00) handled as start of overnight block.
    """
    if t is None or pd.isna(t):
        return None
    for label, start, end in TIME_BLOCKS:
        # Handle the 16:00-24:00 block which ends at 23:59
        if label == "16:00-24:00":
            if t >= start:
                return label
        elif start <= t < end:
            return label
    return "00:00-09:30"  # Catch midnight exactly


def group_stats(df: pd.DataFrame, group_col: str,
               order: list[str] | None = None) -> pd.DataFrame:
    rows = []
    for name, sub in df.groupby(group_col, dropna=False):
        if pd.isna(name):
            continue
        row = {group_col: name, **compute_stats(sub["pnl"]).as_dict()}
        rows.append(row)

    if not rows:
        return pd.DataFrame()

    out = pd.DataFrame(rows)
    if order:
        out[group_col] = pd.Categorical(out[group_col], categories=order, ordered=True)
        out = out.sort_values(group_col).reset_index(drop=True)
    else:
        out = out.sort_values("Total P&L", ascending=False).reset_index(drop=True)
    return out


